'''

Variables in Python:

    - Variables are containers for storing data values.
    - They are created when you assign a value to them.
    - Variable names are case-sensitive and must start with a letter or underscore (_), followed by letters, digits, or underscores.

'''


# Example of variables
x = 5
name = "John"
is_student = True


'''

Numeric Types:
    - int (integer)
    - float (floating-point number)
    - complex (complex number)

Sequence Types:
    - list (ordered collection of items)
    - tuple (ordered, immutable collection of items)
    - str (string)

Mapping Type:
    - dict (collection of key-value pairs)

Set Types:
    - set (unordered collection of unique items)
    - frozenset (immutable set)

'''


# Integer
x = 5

# Float
y = 3.14

# Complex
z = 2 + 3j

# List
my_list = [1, 2, 3, 4, 5]

# Tuple
my_tuple = (10, 20, 30, 40, 50)

# String
my_string = "Hello, World!"

# Dictionary
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# Set
my_set = {1, 2, 3, 4, 5}

# Frozenset
my_frozenset = frozenset({1, 2, 3, 4, 5})


'''

Functions in Python:

    - Functions are blocks of reusable code.
    - They can take input, perform actions, and return output.
    - Python has built-in functions, and you can define your own.

'''